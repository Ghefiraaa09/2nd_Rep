public class Variable04 {
    public static void main(String[] args) {
        //deklarasi
        double bil01, bil02;
        boolean status;

        //input
        bil01 = 100.5;
        bil02 = 10.5;

        //proses
        status = bil01 < bil02;

        //output
        System.out.println("____________________________________");
        System.out.println("                    ");
        System.out.println(bil01 + " < " + bil02);
        System.out.println("Adalah = " + status);
        System.out.println("                ");
        System.out.println("____________________________________");

    }
}
